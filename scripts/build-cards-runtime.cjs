const fs = require('fs');
const path = require('path');
const { loadCards, resolveProjectPath } = require('./pipeline/common.cjs');

const RARITY_ZH_MAP = {
  common: '常见',
  rare: '稀有',
  epic: '史诗',
  legendary: '传说',
};

const TYPE_ZH_MAP = {
  spell: '技能',
  minion: '角色',
  field: '场地',
  weapon: '装备',
};

const FACTION_RUNTIME_MAP = {
  墨家: '玄匠盟',
  儒家: '礼心殿',
};

function mapFaction(faction) {
  return FACTION_RUNTIME_MAP[faction] || faction;
}

function mapRarity(rarity) {
  return RARITY_ZH_MAP[rarity] || '常见';
}

function mapType(type) {
  return TYPE_ZH_MAP[type] || '技能';
}

function pickDisplayType(card) {
  if (typeof card.display_type === 'string' && card.display_type.trim().length > 0) {
    return card.display_type.trim();
  }
  return mapType(card.type);
}

function pickBackground(card) {
  if (Array.isArray(card.lore_anchor_ids) && card.lore_anchor_ids.length > 0) {
    return `锚点: ${card.lore_anchor_ids.join(' / ')}`;
  }
  return '来自 content/cards 的结构化卡牌。';
}

function estimateAttack(card) {
  if (card.type === 'minion') {
    return Math.max(1, Math.min(9, card.cost));
  }
  if (card.type === 'weapon') {
    return Math.max(1, Math.min(6, Math.ceil(card.cost / 2)));
  }
  return undefined;
}

function estimateHp(card) {
  if (card.type === 'minion') {
    return Math.max(1, Math.min(12, card.cost + 2));
  }
  return undefined;
}

function estimateShield(card) {
  if (card.type === 'field') {
    return Math.max(1, Math.min(8, Math.ceil(card.cost / 2) + 1));
  }
  if (card.type === 'weapon') {
    return Math.max(1, Math.min(6, Math.ceil(card.cost / 2)));
  }
  return undefined;
}

function toRuntimeCard(card) {
  return {
    id: card.id,
    name: card.name,
    faction: mapFaction(card.faction),
    type: pickDisplayType(card),
    rarity: mapRarity(card.rarity),
    background: pickBackground(card),
    skill: card.rules_text,
    cost: card.cost,
    attack: estimateAttack(card),
    hp: estimateHp(card),
    shield: estimateShield(card),
    status: card.status || 'active',
  };
}

function buildOutput(runtimeCards, generatedAt) {
  const cardsJson = JSON.stringify(runtimeCards, null, 2);
  return `/* eslint-disable */
// AUTO-GENERATED FILE. DO NOT EDIT.
// Source: content/cards/*.json
// Generated by: scripts/build-cards-runtime.cjs
// Generated at: ${generatedAt}

export type CardStatus = 'active' | 'planned' | 'draft' | 'rework' | 'archived';

export interface CardData {
  id: string;
  name: string;
  faction: string;
  type: string;
  rarity: string;
  background: string;
  skill: string;
  cost: number;
  attack?: number;
  hp?: number;
  shield?: number;
  status: CardStatus;
}

export const rarityColor: Record<string, string> = {
  常见: '#9ca3af',
  稀有: '#60a5fa',
  史诗: '#a855f7',
  传说: '#f59e0b',
};

export const typeColor: Record<string, string> = {
  技能: '#064e3b',
  事件: '#7c3aed',
  场地: '#1e3a5f',
  装备: '#78350f',
  角色: '#831843',
  反制: '#4c1d95',
};

export const CARD_SOURCE_INFO = {
  source: 'content/cards',
  generatedAt: '${generatedAt}',
  cardCount: ${runtimeCards.length},
} as const;

export const CARDS: CardData[] = ${cardsJson};
`;
}

function main() {
  const { cards, parseErrors } = loadCards();
  if (parseErrors.length > 0) {
    console.error('[build-cards-runtime] parse errors found:');
    for (const item of parseErrors) {
      console.error(`- ${item.filePath}: ${item.error}`);
    }
    process.exit(1);
  }

  const runtimeCards = cards
    .map((entry) => entry.data)
    .sort((a, b) => String(a.id).localeCompare(String(b.id)))
    .map(toRuntimeCard);

  const outPath = resolveProjectPath('src', 'generated', 'cardsRuntime.ts');
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  const generatedAt = new Date().toISOString();
  fs.writeFileSync(outPath, buildOutput(runtimeCards, generatedAt), 'utf8');

  console.log(`[build-cards-runtime] PASS -> ${path.relative(process.cwd(), outPath)} (${runtimeCards.length} cards)`);
}

main();
